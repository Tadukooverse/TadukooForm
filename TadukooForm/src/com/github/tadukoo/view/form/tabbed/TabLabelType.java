package com.github.tadukoo.view.form.tabbed;

import javax.swing.JLabel;
import javax.swing.JPanel;

/**
 * Tab Label Type is used to tell what type of label should be used for a tab.
 *
 * @author Logan Ferree (Tadukoo)
 * @version Alpha v.0.3.3
 */
public enum TabLabelType{
	/** Used for a simple {@link JLabel} */
	SIMPLE_LABEL,
	/** Used for a simple {@link JLabel} contained in a {@link JPanel} (makes it so it has a solid background) */
	SIMPLE_PANEL_LABEL
}
